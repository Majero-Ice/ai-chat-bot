export interface CrawlerOptions {
  /**
   * Максимальная глубина обхода (по умолчанию 2)
   */
  maxDepth?: number;
  /**
   * Максимальное количество страниц для обхода (по умолчанию 50)
   */
  maxPages?: number;
  /**
   * Таймаут для загрузки страницы в миллисекундах (по умолчанию 30000)
   */
  timeout?: number;
  /**
   * Ожидание загрузки контента в миллисекундах (по умолчанию 2000)
   */
  waitForContent?: number;
  /**
   * Селектор для основного контента страницы (по умолчанию 'body')
   */
  contentSelector?: string;
  /**
   * Игнорировать ссылки, которые не относятся к тому же домену (по умолчанию true)
   */
  sameDomainOnly?: boolean;
  /**
   * Паттерны URL для исключения (например, ['/login', '/logout'])
   */
  excludePatterns?: string[];
  /**
   * Паттерны URL для включения (если указано, будут обрабатываться только эти паттерны)
   */
  includePatterns?: string[];
}

